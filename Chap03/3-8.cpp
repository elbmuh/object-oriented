#include <iostream>
using namespace std;

class Circle {
public:
	int radius; // 멤버변수
	Circle();   // 생성자
	Circle(int r);
	~Circle();  // 소멸자
	double getArea(); // 멤버함수
};

Circle::Circle() {
	radius = 1;
	cout << "반지름 " << radius << ", 원 생성\n" << endl;
}

Circle::Circle(int r) {
	radius = r;
	cout << "반지름 " << radius << ", 원 생성\n" << endl;
}

Circle::~Circle() {
	cout << "반지름 " << radius << ", 원 소멸\n" << endl;
}

double Circle::getArea() {
	return 3.14 * radius * radius;
}

